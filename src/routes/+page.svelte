<h1>Advent of code 2022</h1>

<h2>What is advent of code?</h2>
<p>It is an advent calender of small programming puzzles released in december as a fun challenge for those interested in programming.</p>
<p>The puzzles are split into two parts and usually increase in difficulty as they progress.</p>
<p>I will attempt to animate any solutions that i think would look cool.</p>

<h2>Why am I doing this?</h2>
<p>It's an exercise for me to solve tricky puzzles, explain problems and their solution.</p>
<p>It's fun! It also presents challenges i might not normally face in my field of development.</p>

